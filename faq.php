      <h2 id="algemeen">Algemeen</h2>
      <p><strong>Hoelang duurt een standaard reparatie?</strong><br>
      Meestal 3–7 werkdagen voor standaard reparaties. Complexe reworks of customisatie kunnen 2–3 weken duren. Tijdens piekperiodes communiceren we een realistische levertijd bij ontvangst.</p>

      <p><strong>Bieden jullie garantie?</strong><br>
      Ja — we bieden 6 maanden garantie op structurele reparaties (constructie, versteviging) en 30 dagen op zichtbare afwerking. Garantie dekt fabricagefouten, niet normaal slijtage.</p>

      <h2 id="materials">Materials & Garen</h2>
      <p><strong>Kunnen jullie garenkleur en dikte matchen?</strong><br>
      Ja. We houden een uitgebreide voorraad garens (diverse diktes en kleuren), inclusief katoenen en polyester blends. Voor bijzondere wassing of zeldzame garens bieden we advies en alternatieven die visueel en functioneel passen.</p>

      <p><strong>Gebruik je originele materialen (zoals YKK-ritsen)?</strong><br>
      Waar mogelijk gebruiken we originele of hoogwaardige vervangende hardware (YKK, koperen knopen). Voor vintagestukken zoeken we naar authentieke vervangingen.</p>

      <h2 id="proces">Reparatieproces (stap-voor-stap)</h2>
      <ul>
        <li><strong>Inspectie & offerte:</strong> stuur foto’s via de <a href="aanvraag.php">aanvraagpagina</a> of kom langs.</li>
        <li><strong>Goedkeuring:</strong>  u ontvangt een prijsindicatie en doorlooptijd; na akkoord starten we.</li>
        <li><strong>Reparatie:</strong>  we gebruiken speciale denim-technieken (Sashiko, hidden mending, bartack reinforcement).</li>
        <li><strong>Finish & kwaliteitscontrole:</strong>  elke reparatie wordt gecontroleerd en gefotografeerd.</li>
        <li><strong>Ophalen/retour:</strong>  ophalen in de studio of verzenden (kosten afhankelijk van afstand).</li>
      </ul>

