      <h2 id="verzenden">Verzenden & Ophalen</h2>
      <p><strong>Verzending:</strong> we accepteren retourzendingen. Verzendkosten zijn voor rekening van de klant tenzij anders afgesproken. We verpakken zorgvuldig om transportschade te vermijden.</p>
      <p><strong>Ophalen:</strong> ophalen kan tijdens openingstijden of op afspraak. Wij vragen u altijd om afspraakbevestiging bij drukke periodes.</p>

      <h2 id="zorg">Zorg & Onderhoud</h2>
      <p><strong>Hoe onderhoud ik mijn gerepareerde denim?</strong><br>
      Was binnenstebuiten op maximaal 30°C en voorkom agressieve droogcycli. Gebruik mild wasmiddel en laat aan de lucht drogen om de reparatie en wassing te behouden.</p>

      <h2 id="custom">Custom Werk & Advisering</h2>
      <p>We doen customisaties (vervlechtingen, sashiko patronen, patches). Voor maatwerk plannen we een consult: breng of stuur foto’s en we bespreken stof, garen en stijlopties.</p>

      <h2 id="garantie">Garantie & Retourbeleid</h2>
